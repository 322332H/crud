 private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try
        {
            PreparedStatement ps = cn.prepareStatement
        ("UPDATE user SET Names ='" + txtName.getText() +"',Surname='" + txtSurname.getText() + "',Email='" + txtEmail.getText() + "',DocumentNumber='" + txtDocument.getText() +"',BirthDate='" + txtDate.getText() + "' WHERE Id = '" + txtId.getText() + "' ");
            
            int indice = ps.executeUpdate();
            
            if(indice > 0){
             JOptionPane.showMessageDialog(rootPane, "DATA WAS SUCCESSFULLY UPDATED");
             cleanPage();
             showData();
            }else{
                JOptionPane.showMessageDialog(null, "NO ROW WAS SELECTED");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "ERROR TO UPDATED THE INFORMATION" + e);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed
