    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed
        
         try
      {
        PreparedStatement ps = cn.prepareStatement("INSERT INTO user(Names, Surname, Email, DocumentNumber,BirthDate) VALUES (?,?,?,?,?)");
        ps.setString(1, txtName.getText());
        ps.setString(2, txtSurname.getText());
        ps.setString(3, txtEmail.getText());
        ps.setString(4, txtDocument.getText());
        ps.setString(5, txtDate.getText());
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "DATA WAS SAVE SUCCESSFULLY");
        showData();
        cleanPage();
      }catch(SQLException e)
      {
          System.out.println("ERROR TO REGISTER A NEW USER" + e);
      } 
         
         
    }//GEN-LAST:event_btnregisterActionPerformed
